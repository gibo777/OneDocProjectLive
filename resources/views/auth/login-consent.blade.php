<p class="mt-2">1.3 Data Usage and Disclosure:</p>
<p>One Document Corporation will not use or disclose the data for purposes other than what it was
collected for, except with consent or as required by Philippine laws. Information may be shared with
trusted third parties such as legal advisors, regulatory bodies, and their authorized representatives.</p>
<p>1.4 Data Security:</p>
<p>One Document Corporation employs measures to safeguard the data collected, including user
restriction levels and training. All data that is captured will be stored in an encrypted database.</p>
<p>1.5 Data Retention Policy:</p>
<p>Information is retained only as necessary to provide for the purposes outlined in this policy or as
required under Philippine laws and regulations. When the data is no longer needed, it will be
securely deleted.</p>
<p>1.5.1 Uploaded Identification Photo:</p>
<p>Identification photo refers to the formal photos required for upload. These ID photos will be
uploaded into the system for Personal Data and will be retained for up to three (3) years after the
separation of the employee from the company.</p>
<p>1.5.2 Employee Record:</p>
<p>Employee Record refers to the overall collated information collected from the employees. The
Employee Record will be retained for up to three (3) years after the separation of the employee
from the company.</p>
<p>1.5.3 Biometric Images:</p>
<p>Biometric images refer to images captured during Timekeeping, to verify accuracy and truthfulness.
These images will be retained for six (6) months.</p>
<p>1.6 Rights of Data Subjects:</p>
<p>One Document Corporation employees have the right to access, amend, and delete the data
collected and stored by the company in this policy. Requests should be made formally to the One
Document Corporation HR Department, and the HR Department will respond by applicable laws and
regulations.</p>
<p>1.7 Changes to the Policy:</p>
<p>One Document Corporation reserves the right to update this policy as deemed necessary due to
legal requirements, new enhancements, or changes in processes within the HRIS Employee Portal
and its modules. In the event of an update, such changes will be communicated to the employees
through an inter-company memo, along with the updated version of this policy.</p>
<p>1.8 Contact Information:</p>
<p>For inquiries and concerns related to the contents of this policy or your collected data, please
contact our Data Protection Officer at <link>dpo@onedoc.ph</link>.</p>
<p>1.9 Acknowledgment:</p>

<p>By using One Document Corporation’s HRIS Employee Portal, you:</p>
<p class="pl-3">- Acknowledge that you have read, understood, and agreed to the information and practices outlined
in this Data Privacy Policy.</p>
